static inline void
opaque_format(struct adata *ad, byte *buf, unsigned int size)
{
  byte *bound = buf + size - 10;
  int i;

  for(i = 0; i < ad->length; i++)
    {
      if (buf > bound)
	{
	  strcpy(buf, " ...");
	  return;
	}
      if (i)
	*buf++ = ' ';

      buf += bsprintf(buf, "%02x", ad->data[i]);
    }

  *buf = 0;
  return;
}
